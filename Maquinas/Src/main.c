void led1_process(){
	switch(st_led1){
	case LED_STB:
		break;
	case LED_PAR:
		if(timers[TLED_1]==0){
		timers[TLED_1]=100;
		HAL_GPIO_TogglePin(GPIOA,GPIO_PIN_4);
		}
		st_led1=LED_CHECK;
		break;
	case LED_CHECK:
		if(timers[TLED1_T]==0){
			st_led1=LED_STB;
			HAL_GPIO_WritePin(GPIOA,GPIO_PIN_4,0);
		}
		st_led1=LED_PAR;
		break;
	}
}
void boton1_process(){
	switch(st_p1){
	case P_CHECK:
		  if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_12)==1){
			  if(st_led1==LED_PAR){
					sprintf(texto,"Tiempo restante boton 1: %d ms\n",timers[TLED1_T]);
					CDC_Transmit_FS(texto,strlen(texto));
					st_p1=P_PUSH;
					timers[TP]=200;
			  }else{
				  st_led1=LED_PAR;
				  timers[TLED1_T]=3000;
				  st_p1=P_PUSH;
				  timers[TP]=200;
			  }
		  }
		break;
	case P_PUSH:
			if(timers[TP]==0){
				st_p1=P_CHECK;
				timers[TP]=200;
			}
			break;
	}
}
